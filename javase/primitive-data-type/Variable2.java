public class Variable2 
{
  public static void main(String[] args)
  {
    // double data type
    double d = 1.2;
    System.out.println(d);

    // float data type
    //float f = 1.2; // error, because 1.2 is double data type
    float f = 1.2F;
    System.out.println(f);

    // char data type
    char a = '9';
    System.out.println(a);

    // boolean data type
    boolean b = false;
    System.out.println(b);
  
    float fvar = (float)1.1;
    float fvar2 = 1.1F;
    System.out.println(fvar);
    System.out.println(fvar2);
  }
}